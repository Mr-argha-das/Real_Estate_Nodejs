const mongoose = require("mongoose");

const servicePageSchema = new mongoose.Schema(
  {
    image: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ServicePage = mongoose.model("ServicePage", servicePageSchema);

module.exports = ServicePage;
